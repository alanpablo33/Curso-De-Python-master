nome= "Alan " # e uma String (s)
idade = 25 #e um inteiro (d)
pi= 3.14664124544265315
#colocamos o %s para indicar a str , e depois %variavel que deseja chavar
print ("Meu Nome e %s" %nome)
# colocamos o %s para indicar a str , %d para indicar o int , depois colocamos %(nome , idade) en sequencia chamando as variaveis
print('Meu Nome e %stenho %d' %(nome , idade))
# colocamos %f para float , chamamos a variavel
print("O Valor de pi é %f" %pi)
# colocamos %.numero de casas que queremos mostrar 
print("O valor de pi é %.1f " %pi)
